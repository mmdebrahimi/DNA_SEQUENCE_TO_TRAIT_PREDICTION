#!/usr/bin/env python
"""genome_jepa_prototype.py - a minimal, CORRECT genome-JEPA (Joint-Embedding Predictive Architecture).

WHAT THIS IS (honest scope):
  A laptop/CPU-scale PROTOTYPE that implements the LeCun-style JEPA mechanism on DNA sequence:
  predict the EMBEDDING of a masked genomic window from the surrounding context's embedding, in LATENT
  space, using an EMA target-encoder + stop-gradient to AVOID representation collapse (the load-bearing
  JEPA detail; a naive "predict your own embedding" collapses to a constant).

  It is NOT a validated genome model and NOT a phenotype predictor. dna_decode's evidence is that learned
  embedding -> PHENOTYPE is a 0-for-5 de-confounded negative. JEPA is REPRESENTATION learning; its value is
  gated on a downstream falsifier (does the JEPA rep beat frozen-FM embeddings + an LD baseline on
  masked-SNP imputation / variant-effect on a de-confounded slice?) -- see the plan doc. This script only
  proves the MECHANISM: JEPA trains on sequence data with local structure, loss decreases, and the
  representation does NOT collapse.

DESIGN (I-JEPA adapted to 1D sequence):
  - context_encoder (online, trained by grad)   : sequence -> per-position latent [B, L, D]
  - target_encoder  (EMA copy, stop-gradient)   : sequence -> per-position latent [B, L, D]
  - predictor                                   : pooled context latent -> predicted target-block latents
  - loss = MSE(predictor(context_repr), sg(target_encoder(x)[target_block]))
  - after each step: target_encoder <- ema * target_encoder + (1-ema) * context_encoder
  - anti-collapse monitor: variance of the TARGET embeddings across the batch must stay > eps.

Run:  python scripts/genome_jepa_prototype.py         (trains structured vs shuffled, asserts + prints)
"""
from __future__ import annotations

import sys
import copy

import torch
import torch.nn as nn
import torch.nn.functional as F

try:
    sys.stdout.reconfigure(encoding="utf-8")   # cp1252 console safety (this host)
except Exception:
    pass

VOCAB = 4  # A C G T


# ------------------------- synthetic data with LEARNABLE local structure -------------------------

def make_markov_dna(n: int, L: int, *, order: int = 2, seed: int = 0) -> torch.Tensor:
    """n sequences of length L over {0..3}, generated by an order-`order` Markov chain with a FIXED
    random transition table -> genuine local dependency, so context predicts the target region better
    than chance (the signal JEPA should capture). Returns LongTensor [n, L]."""
    g = torch.Generator().manual_seed(seed)
    # transition logits keyed by the previous `order` symbols -> next-symbol distribution
    n_ctx = VOCAB ** order
    logits = torch.randn(n_ctx, VOCAB, generator=g) * 2.5  # peaky -> strong dependency
    probs = torch.softmax(logits, dim=-1)
    out = torch.zeros(n, L, dtype=torch.long)
    # random seed context per sequence
    out[:, :order] = torch.randint(0, VOCAB, (n, order), generator=g)
    for t in range(order, L):
        key = torch.zeros(n, dtype=torch.long)
        for j in range(order):
            key = key * VOCAB + out[:, t - order + j]
        p = probs[key]  # [n, VOCAB]
        out[:, t] = torch.multinomial(p, 1, generator=g).squeeze(1)
    return out


def shuffle_positions(x: torch.Tensor, seed: int = 1) -> torch.Tensor:
    """Destroy local structure by permuting each sequence's positions independently -> a negative control
    (marginal base composition preserved, dependency removed). JEPA should predict the target block WORSE
    here than on the structured data if it is learning real dependency, not collapsing."""
    g = torch.Generator().manual_seed(seed)
    n, L = x.shape
    out = x.clone()
    for i in range(n):
        perm = torch.randperm(L, generator=g)
        out[i] = x[i, perm]
    return out


# ------------------------- the JEPA model -------------------------

class SeqEncoder(nn.Module):
    """Tiny sequence encoder: embed -> depthwise-ish conv stack -> LayerNorm -> per-position latent [B,L,D].
    No BatchNorm (avoids a trivial collapse pathway); LayerNorm only."""

    def __init__(self, d_model: int = 32, n_layers: int = 2, kernel: int = 5):
        super().__init__()
        self.embed = nn.Embedding(VOCAB, d_model)
        self.convs = nn.ModuleList(
            [nn.Conv1d(d_model, d_model, kernel, padding=kernel // 2) for _ in range(n_layers)]
        )
        self.norm = nn.LayerNorm(d_model)

    def forward(self, x: torch.Tensor) -> torch.Tensor:  # x: [B, L] long
        h = self.embed(x).transpose(1, 2)                # [B, D, L]
        for conv in self.convs:
            h = h + F.gelu(conv(h))                      # residual
        h = h.transpose(1, 2)                            # [B, L, D]
        return self.norm(h)


class Predictor(nn.Module):
    """Predict the target block's per-position latents from a pooled context representation + a learned
    positional query for each target position."""

    def __init__(self, d_model: int = 32, block: int = 8):
        super().__init__()
        self.block = block
        self.pos_query = nn.Parameter(torch.randn(block, d_model) * 0.02)
        self.net = nn.Sequential(
            nn.Linear(d_model * 2, d_model * 2), nn.GELU(), nn.Linear(d_model * 2, d_model)
        )

    def forward(self, ctx_pool: torch.Tensor) -> torch.Tensor:  # ctx_pool: [B, D]
        B = ctx_pool.shape[0]
        q = self.pos_query.unsqueeze(0).expand(B, -1, -1)                 # [B, block, D]
        c = ctx_pool.unsqueeze(1).expand(-1, self.block, -1)             # [B, block, D]
        return self.net(torch.cat([c, q], dim=-1))                       # [B, block, D]


class GenomeJEPA(nn.Module):
    def __init__(self, d_model: int = 32, block: int = 8, ema: float = 0.99):
        super().__init__()
        self.context_encoder = SeqEncoder(d_model)
        self.target_encoder = copy.deepcopy(self.context_encoder)
        for p in self.target_encoder.parameters():
            p.requires_grad_(False)                      # target is EMA-updated, never grad-trained
        self.predictor = Predictor(d_model, block)
        self.block = block
        self.ema = ema

    @torch.no_grad()
    def update_target(self):
        for tp, cp in zip(self.target_encoder.parameters(), self.context_encoder.parameters()):
            tp.mul_(self.ema).add_(cp.detach(), alpha=1 - self.ema)

    def forward(self, x: torch.Tensor, start: int):
        """Mask the contiguous target block [start, start+block); predict its target-embeddings from the
        pooled context (everything OUTSIDE the block). Returns (loss, target_variance)."""
        B, L = x.shape
        blk = self.block
        end = start + blk
        # --- target path: EMA encoder on the FULL sequence, take the block, STOP-GRADIENT ---
        with torch.no_grad():
            tgt_full = self.target_encoder(x)            # [B, L, D]
            target = tgt_full[:, start:end, :].detach()  # [B, blk, D]
        # --- context path: online encoder, mask the block by mean-pooling the VISIBLE positions ---
        ctx_full = self.context_encoder(x)               # [B, L, D]
        mask = torch.ones(L, dtype=torch.bool)
        mask[start:end] = False
        ctx_pool = ctx_full[:, mask, :].mean(dim=1)      # [B, D]  (context = outside the block)
        pred = self.predictor(ctx_pool)                  # [B, blk, D]
        loss = F.mse_loss(pred, target)
        # anti-collapse: variance of target embeddings across the batch (per-dim), averaged
        tvar = target.reshape(-1, target.shape[-1]).var(dim=0).mean()
        return loss, tvar.detach()


# ------------------------- training -------------------------

def train_prototype(x: torch.Tensor, *, steps: int = 300, lr: float = 1e-3, d_model: int = 32,
                    block: int = 8, seed: int = 0):
    torch.manual_seed(seed)
    model = GenomeJEPA(d_model=d_model, block=block)
    opt = torch.optim.Adam(
        list(model.context_encoder.parameters()) + list(model.predictor.parameters()), lr=lr
    )
    L = x.shape[1]
    g = torch.Generator().manual_seed(seed)
    hist = {"loss": [], "tvar": []}
    model.train()
    for step in range(steps):
        start = int(torch.randint(0, L - block + 1, (1,), generator=g).item())
        loss, tvar = model(x, start)
        opt.zero_grad(); loss.backward(); opt.step()
        model.update_target()
        hist["loss"].append(float(loss.detach())); hist["tvar"].append(float(tvar))
    return model, hist


def main() -> int:
    torch.manual_seed(0)
    N, L = 256, 64
    struct = make_markov_dna(N, L, order=2, seed=0)
    shuf = shuffle_positions(struct, seed=1)

    _, h_struct = train_prototype(struct, steps=300, seed=0)
    _, h_shuf = train_prototype(shuf, steps=300, seed=0)

    def tail(v, k=20):
        return sum(v[-k:]) / len(v[-k:])

    s_loss0, s_lossF = tail(h_struct["loss"][:20]), tail(h_struct["loss"])
    sh_lossF = tail(h_shuf["loss"])
    s_tvarF = tail(h_struct["tvar"])

    print("=== genome-JEPA prototype (CPU) ===")
    print(f"structured: loss start={s_loss0:.4f} -> final={s_lossF:.4f}  (decrease={s_loss0 - s_lossF:.4f})")
    print(f"shuffled  : final loss={sh_lossF:.4f}  (negative control)")
    print(f"anti-collapse: final target-embedding variance={s_tvarF:.4f}  (>0 => NOT collapsed)")
    print(f"structure signal: shuffled_loss - structured_loss = {sh_lossF - s_lossF:+.4f}"
          f"  ({'structured predicts better' if sh_lossF > s_lossF else 'NO structure captured'})")

    ok = True
    if not (s_lossF < s_loss0):
        print("FAIL: loss did not decrease"); ok = False
    if not (s_tvarF > 1e-3):
        print("FAIL: representation COLLAPSED (target variance ~ 0)"); ok = False
    if not (sh_lossF > s_lossF):
        print("FAIL: no learnable structure captured (shuffled <= structured)"); ok = False
    if ok:
        print("\nPASS: JEPA mechanism verified - trains, does NOT collapse, captures local dependency.")
        print("NOTE: mechanism only. Value on a real downstream (imputation/variant-effect vs frozen-FM +")
        print("      LD baselines, de-confounded) is the pre-registered falsifier -- workhorse-GPU-gated.")
    return 0 if ok else 1


if __name__ == "__main__":
    sys.exit(main())
